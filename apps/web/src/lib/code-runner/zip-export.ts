function generateEnvExample(files: ExportFile[]): string {
  const envVars = new Set<string>();
  const envPattern = /(?:process\.env|import\.meta\.env)\.([A-Z_][A-Z0-9_]*)/g;
  for (const file of files) {
    let match: RegExpExecArray | null;
    while ((match = envPattern.exec(file.content)) !== null) {
      if (match[1] !== 'NODE_ENV') {
        envVars.add(match[1]);
      }
    }
  }

  const lines = [
    '# Copy this file to .env and fill in your values',
    '# Never commit .env to version control',
    '',
  ];

  if (envVars.size === 0) {
    lines.push('DATABASE_URL=postgresql://localhost:5432/myapp');
  } else {
    for (const v of Array.from(envVars).sort()) {
      lines.push(`${v}=`);
    }
  }
  return lines.join('\n') + '\n';
}

