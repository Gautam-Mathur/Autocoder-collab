// ── Core export functions ──────────────────────────────────────────────────────

export async function exportProjectAsZip(
  files: ExportFile[],
  options: ExportOptions
): Promise<Blob> {
  const zip = new JSZip();
  const rootDir = options.projectName
    .replace(/[^a-z0-9-_]/gi, '-')
    .toLowerCase()
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || 'project';

  const folder = zip.folder(rootDir)!;

  // Add all project files
  for (const file of files) {
    folder.file(file.path, file.content);
  }

  // Add optional boilerplate files
  if (options.includeReadme) {
    folder.file('README.md', generateReadme(options.projectName, files));
  }
  if (options.includeGitignore) {
    folder.file('.gitignore', generateGitignore());
  }
  if (options.includeEnvExample) {
    folder.file('.env.example', generateEnvExample(files));
  }
  if (options.includeLicense) {
    folder.file('LICENSE', generateLicense(options.projectName));
  }

  return zip.generateAsync({
    type: 'blob',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
  });
}

export function downloadBlob(blob: Blob, filename: string): void {
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.style.display = 'none';
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  // Revoke after a delay to allow the download to start
  setTimeout(() => URL.revokeObjectURL(url), 15_000);
}

export async function downloadProjectAsZip(
  files: ExportFile[],
  projectName: string
): Promise<void> {
  const safeName = (projectName || 'project')
    .replace(/[^a-z0-9-_]/gi, '-')
    .toLowerCase()
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || 'project';

  const blob = await exportProjectAsZip(files, {
    projectName: safeName,
    includeReadme: true,
    includeGitignore: true,
    includeLicense: false,
    includeEnvExample: true,
    format: 'zip',
  });

  downloadBlob(blob, `${safeName}.zip`);
}
