import JSZip from 'jszip';

export interface ExportFile {
  path: string;
  content: string;
  language?: string;
}

export interface ExportOptions {
  projectName: string;
  includeReadme: boolean;
  includeGitignore: boolean;
  includeLicense: boolean;
  includeEnvExample: boolean;
  format: 'zip' | 'tar';
}

// ── Boilerplate file generators ────────────────────────────────────────────────

function generateReadme(projectName: string, files: ExportFile[]): string {
  const hasPackageJson = files.some(f => f.path.includes('package.json'));
  const hasServer = files.some(f => f.path.includes('server'));
  const hasPython = files.some(f => f.path.endsWith('.py'));

  let readme = `# ${projectName}\n\nGenerated by AutoCoder — AI-Powered Code Generator\n\n`;
  readme += `## Project Structure\n\n\`\`\`\n${files.map(f => f.path).join('\n')}\n\`\`\`\n\n`;
  readme += `## Getting Started\n\n`;

  if (hasPackageJson) {
    readme += `### Install dependencies\n\n\`\`\`bash\nnpm install\n\`\`\`\n\n`;
    readme += `### Start development server\n\n\`\`\`bash\nnpm run dev\n\`\`\`\n\n`;
  }

  if (hasPython) {
    readme += `### Python setup\n\n\`\`\`bash\npip install -r requirements.txt\npython main.py\n\`\`\`\n\n`;
  }

  if (hasServer) {
    readme += `The app runs on **http://localhost:5000** by default.\n\n`;
  }

  readme += `## Environment Variables\n\nCopy \`.env.example\` to \`.env\` and fill in your values before starting.\n`;
  return readme;
}

