// Rust Backend Template (Actix-web)
export function generateRustBackend(projectName: string): BackendTemplate {
  const crateName = projectName.toLowerCase().replace(/\s+/g, '_');
  const files: { path: string; content: string; language: string }[] = [];

  files.push({
    path: 'Cargo.toml',
    content: `[package]
name = "${crateName}"
version = "0.1.0"
edition = "2021"

[dependencies]
actix-web = "4"
actix-cors = "0.6"
serde = { version = "1.0", features = ["derive"] }
serde_json = "1.0"
tokio = { version = "1", features = ["macros", "rt-multi-thread"] }
chrono = { version = "0.4", features = ["serde"] }
dotenv = "0.15"
env_logger = "0.10"
log = "0.4"
`,
    language: 'toml'
  });

  files.push({
    path: 'src/main.rs',
    content: `use actix_cors::Cors;
use actix_web::{web, App, HttpResponse, HttpServer, Responder};
use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use std::collections::HashMap;
use std::sync::Mutex;

#[derive(Debug, Clone, Serialize, Deserialize)]
struct Item {
    id: u32,
    name: String,
    description: Option<String>,
    created_at: DateTime<Utc>,
}

#[derive(Debug, Deserialize)]
struct CreateItem {
    name: String,
    description: Option<String>,
}

struct AppState {
    items: Mutex<HashMap<u32, Item>>,
    counter: Mutex<u32>,
}

async fn index() -> impl Responder {
    HttpResponse::Ok().json(serde_json::json!({
        "message": "Welcome to ${projectName} API",
        "version": "1.0.0"
    }))
}

async fn health() -> impl Responder {
    HttpResponse::Ok().json(serde_json::json!({
        "status": "healthy",
        "timestamp": Utc::now().to_rfc3339()
    }))
}

async fn list_items(data: web::Data<AppState>) -> impl Responder {
    let items = data.items.lock().unwrap();
    let result: Vec<Item> = items.values().cloned().collect();
    HttpResponse::Ok().json(result)
}

async fn create_item(
    data: web::Data<AppState>,
    body: web::Json<CreateItem>,
) -> impl Responder {
    let mut counter = data.counter.lock().unwrap();
    let mut items = data.items.lock().unwrap();

    *counter += 1;
    let item = Item {
        id: *counter,
        name: body.name.clone(),
        description: body.description.clone(),
        created_at: Utc::now(),
    };

    items.insert(*counter, item.clone());
    HttpResponse::Created().json(item)
}

async fn get_item(
    data: web::Data<AppState>,
    path: web::Path<u32>,
) -> impl Responder {
    let items = data.items.lock().unwrap();
    let id = path.into_inner();

    match items.get(&id) {
        Some(item) => HttpResponse::Ok().json(item),
        None => HttpResponse::NotFound().json(serde_json::json!({"error": "Item not found"})),
    }
}

async fn update_item(
    data: web::Data<AppState>,
    path: web::Path<u32>,
    body: web::Json<CreateItem>,
) -> impl Responder {
    let mut items = data.items.lock().unwrap();
    let id = path.into_inner();

    match items.get_mut(&id) {
        Some(item) => {
            item.name = body.name.clone();
            item.description = body.description.clone();
            HttpResponse::Ok().json(item.clone())
        }
        None => HttpResponse::NotFound().json(serde_json::json!({"error": "Item not found"})),
    }
}

async fn delete_item(
    data: web::Data<AppState>,
    path: web::Path<u32>,
) -> impl Responder {
    let mut items = data.items.lock().unwrap();
    let id = path.into_inner();

    match items.remove(&id) {
        Some(_) => HttpResponse::Ok().json(serde_json::json!({"message": "Item deleted"})),
        None => HttpResponse::NotFound().json(serde_json::json!({"error": "Item not found"})),
    }
}

#[actix_web::main]
async fn main() -> std::io::Result<()> {
    dotenv::dotenv().ok();
    env_logger::init();

    let app_state = web::Data::new(AppState {
        items: Mutex::new(HashMap::new()),
        counter: Mutex::new(0),
    });

    let port = std::env::var("PORT").unwrap_or_else(|_| "8080".to_string());

    log::info!("Starting server on port {}", port);

    HttpServer::new(move || {
        let cors = Cors::permissive();

        App::new()
            .wrap(cors)
            .app_data(app_state.clone())
            .route("/", web::get().to(index))
            .route("/health", web::get().to(health))
            .service(
                web::scope("/api")
                    .route("/items", web::get().to(list_items))
                    .route("/items", web::post().to(create_item))
                    .route("/items/{id}", web::get().to(get_item))
                    .route("/items/{id}", web::put().to(update_item))
                    .route("/items/{id}", web::delete().to(delete_item))
            )
    })
    .bind(format!("0.0.0.0:{}", port))?
    .run()
    .await
}
`,
    language: 'rust'
  });

  files.push({
    path: 'README.md',
    content: `# ${projectName}

Rust backend with Actix-web framework generated by AutoCoder.

## Getting Started

\`\`\`bash
# Build and run
cargo run

# Build for production
cargo build --release
\`\`\`

Server runs on http://localhost:8080
`,
    language: 'markdown'
  });

  return {
    language: 'rust',
    framework: 'Actix-web',
    files
  };
}

